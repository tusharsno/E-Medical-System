"use client";
import { motion, useReducedMotion } from "framer-motion";
import { useState, useEffect } from "react";
import { ArrowRight, LayoutDashboard } from "lucide-react";

function cn(...classes: string[]) {
  return classes.filter(Boolean).join(" ").replace(/\s+/g, " ");
}

export default function Hero() {
  const prefersReducedMotion = useReducedMotion();
  const [mounted, setMounted] = useState(false);
  const [particles, setParticles] = useState<
    { x: number; y: number; duration: number; direction: number; randomOffset: number }[]
  >([]);

  // Headline টেক্সটকে শব্দে ভাগ করা
  const headline = "Digital healthcare, delivered without complexity";
  const words = headline.split(" ");

  useEffect(() => {
    setMounted(true);
    if (!prefersReducedMotion) {
      const generated = [...Array(10)].map(() => ({
        x: Math.random() * (typeof window !== 'undefined' ? window.innerWidth : 1000),
        y: Math.random() * (typeof window !== 'undefined' ? window.innerHeight : 800),
        duration: Math.random() * 18 + 18,
        direction: Math.random() > 0.5 ? 1 : -1,
        randomOffset: Math.random() * 100 - 50,
      }));
      setParticles(generated);
    }
  }, [prefersReducedMotion]);

  const ParticlesBackground = () => {
    if (!mounted || prefersReducedMotion) return null;
    return (
      <div className="absolute inset-0 -z-10 overflow-hidden">
        {particles.map((p, i) => (
          <motion.div
            key={i}
            className="absolute w-1.5 h-1.5 bg-gradient-to-r from-blue-400 to-purple-400 rounded-full opacity-20"
            initial={{ x: p.x, y: p.y }}
            animate={{
              x: p.x + p.direction * 180,
              y: p.y + p.randomOffset,
            }}
            transition={{
              duration: p.duration,
              repeat: Infinity,
              repeatType: "reverse",
              ease: "easeInOut",
            }}
          />
        ))}
      </div>
    );
  };

  return (
    <section className="relative min-h-screen overflow-hidden bg-[#F8FAFC] dark:bg-black flex items-center justify-center">
      {/* BACKGROUND */}
      <div className="absolute inset-0">
        <div className={cn(
          "absolute inset-0 bg-gradient-to-br from-indigo-100/60 via-[#F8FAFC] to-blue-100/60",
          "dark:from-indigo-700/40 dark:via-black dark:to-blue-700/40"
        )} />

        {/* Floating Blobs from your reference code */}
        <div className="absolute -top-32 -left-32 w-72 h-72 bg-gradient-to-br from-blue-400 via-purple-400 to-pink-400 opacity-20 rounded-full blur-3xl -z-10 animate-pulse" />
        <div className="absolute -bottom-32 -right-32 w-72 h-72 bg-gradient-to-tr from-pink-400 via-purple-400 to-blue-400 opacity-20 rounded-full blur-3xl -z-10 animate-pulse" />

        <div className={cn(
          "absolute inset-0 bg-[radial-gradient(circle_at_top_left,rgba(99,102,241,0.18),transparent_55%)]",
          "dark:bg-[radial-gradient(circle_at_top_left,rgba(99,102,241,0.25),transparent_50%)]"
        )} />
        
        <ParticlesBackground />
      </div>

      <div className="relative z-10 max-w-6xl mx-auto px-6 text-center">
        {/* Animated Badge */}
        <motion.div 
          initial={{ opacity: 0, y: -20 }}
          animate={{ opacity: 1, y: 0 }}
          transition={{ duration: 0.5 }}
          className={cn(
            "inline-flex items-center gap-2 px-4 py-2 rounded-full",
            "bg-white/80 dark:bg-white/10 backdrop-blur border border-black/10 dark:border-white/10",
            "text-sm text-slate-700 dark:text-white/80 mb-10 shadow-sm"
          )}
        >
          Trusted by modern healthcare teams →
        </motion.div>

        {/* Word-by-word Animated Headline */}
        <h1 className="text-5xl md:text-6xl lg:text-7xl font-medium tracking-tight text-slate-900 dark:text-white mb-6">
          {words.map((word, index) => (
            <motion.span
              key={index}
              initial={{ opacity: 0, filter: "blur(8px)", y: 20 }}
              animate={{ opacity: 1, filter: "blur(0px)", y: 0 }}
              transition={{
                type: "spring",
                stiffness: 70,
                damping: 15,
                delay: index * 0.12,
              }}
              className="inline-block mr-3"
            >
              {word}
            </motion.span>
          ))}
        </h1>

        {/* Animated Underline */}
        <motion.div
          className="h-1 w-32 bg-gradient-to-r from-blue-500 to-indigo-500 mx-auto mt-2 mb-8 rounded"
          initial={{ width: 0 }}
          animate={{ width: "8rem" }}
          transition={{ duration: 1, delay: 1 }}
        />

        {/* Subtitle */}
        <motion.p 
          initial={{ opacity: 0, y: 10 }}
          animate={{ opacity: 1, y: 0 }}
          transition={{ duration: 0.6, delay: 1.2 }}
          className="max-w-2xl mx-auto text-lg text-slate-600 dark:text-white/60"
        >
          Secure consultations, digital prescriptions, and patient records —
          designed to work at modern scale.
        </motion.p>

        {/* CTA Buttons with Hover Effects */}
        <motion.div 
          initial={{ opacity: 0, y: 20 }}
          animate={{ opacity: 1, y: 0 }}
          transition={{ duration: 0.5, delay: 1.5 }}
          className="mt-14 flex flex-col sm:flex-row gap-4 justify-center items-center"
        >
          <motion.a
            href="/register"
            whileHover={{ scale: 1.05 }}
            whileTap={{ scale: 0.98 }}
            className={cn(
              "group relative px-8 py-4 bg-gradient-to-r from-blue-600 to-indigo-600 text-white font-semibold rounded-full",
              "shadow-lg flex items-center gap-2 overflow-hidden"
            )}
          >
            Get Started Free <ArrowRight size={18} />
            <span className="absolute inset-0 bg-gradient-to-r from-transparent via-white/20 to-transparent -translate-x-full group-hover:translate-x-full transition-transform duration-1000" />
          </motion.a>

          <motion.a
            href="/login"
            whileHover={{ scale: 1.05 }}
            whileTap={{ scale: 0.98 }}
            className={cn(
              "px-8 py-4 rounded-full font-semibold bg-white text-slate-900 border border-slate-300",
              "hover:border-indigo-500 transition dark:bg-white/10 dark:text-white dark:border-white/20 flex items-center gap-2"
            )}
          >
            <LayoutDashboard size={18} /> Login to Dashboard
          </motion.a>
        </motion.div>
      </div>
    </section>
  );
}